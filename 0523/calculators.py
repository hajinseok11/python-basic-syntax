a = 3
b = 4
# 덧셈 : +, 빼기 : - , 곱하기 : *, 나누기 : /, 몪 : //, 나머지 : %
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)

# 제곱, 제곱근
# 2의 10 제곱을 출력하라
print(2**10)
print(pow(2,10))
import math
print(math.pow(2,10))
# 1024의 제곱근을 구하라
# 제곱근은 math라는 라이브러리를 import 해야한다.
print(math.sqrt(1024))